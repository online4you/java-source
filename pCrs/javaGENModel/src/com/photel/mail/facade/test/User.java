package com.photel.mail.facade.test;
public class User {
    String trcNombre;
    String trcDoc;
    String id;
    public User(String nombre, String dni, String id) {
	this.trcNombre = nombre;
	this.trcDoc = dni;
	this.id = id;
    }
	public String getTrcNombre() {
		return trcNombre;
	}
	public void setTrcNombre(String trcNombre) {
		this.trcNombre = trcNombre;
	}
	public String getTrcDoc() {
		return trcDoc;
	}
	public void setTrcDoc(String trcDoc) {
		this.trcDoc = trcDoc;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}

    
}

